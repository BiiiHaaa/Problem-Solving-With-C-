#include <iostream>
using namespace std;

int main() {
    for(int i = 0;i<15 ;i++) {
        if(i>=9 && i<=15) {
            cout << "==============================================" << endl;
        }else if(i%2==0) {
            cout << "* * * * * * ==================================" << endl;
        }else {
            cout << " * * * * *  ==================================" << endl;
        }
    }
}
